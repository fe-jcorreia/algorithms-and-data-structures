from kruskal import Kruskal

class TestKruskal():
  def run_all_tests(self):
    self.test_small_graph()
    self.test_medium_graph()
    self.test_large_graph()
  
  def test_small_graph(self):
    graph = [
      [(0,1,8), (0,2,5)],
      [(1,0,8), (1,2,9), (1,3,11)],
      [(2,0,5), (2,1,9), (2,3,15), (2,4,10)],
      [(3,1,11), (3,2,15), (3,4,7)],
      [(4,2,10), (4,3,7)],
    ]

    kruskal = Kruskal()
    kruskal.execute(graph)

  def test_medium_graph(self):
    graph = [
      [(0,1,4), (0,7,8)],
      [(1,0,4), (1,2,8), (1,7,11)],
      [(2,1,8), (2,3,7), (2,5,4), (2,8,2)],
      [(3,2,7), (3,4,9), (3,5,14)],
      [(4,3,9), (4,5,10)],
      [(5,2,4), (5,3,14), (5,4,10), (5,6,2)],
      [(6,5,2), (6,7,1), (6,8,6)],
      [(7,0,8), (7,1,11), (7,6,1), (7,8,7)],
      [(8,2,2), (8,6,6), (8,7,7)],
    ]

    kruskal = Kruskal()
    kruskal.execute(graph)

  def test_large_graph(self):
    graph = [
      [(0,1,3), (0,4,12), (0,12,1), (0,13,6)],
      [(1,0,3), (1,3,2), (1,12,5)],
      [(2,6,5), (2,7,10), (2,13,2), (2,14,13)],
      [(3,1,2), (3,4,3), (3,6,1)],
      [(4,0,12), (4,3,3), (4,10,2), (4,12,1)],
      [(5,8,6), (5,9,1), (5,10,14)],
      [(6,2,5), (6,3,1), (6,7,1), (6,8,2)],
      [(7,2,10), (7,6,1), (7,14,4)],
      [(8,5,6), (8,6,2), (8,9,3)],
      [(9,5,1), (9,8,3), (9,10,5)],
      [(10,4,2), (10,5,14), (10,9,5), (10,11,1)],
      [(11,10,1), (11,12,5)],
      [(12,0,1), (12,1,5), (12,4,1), (12,11,5)],
      [(13,0,6), (13,2,2), (13,14,3)],
      [(14,2,13), (14,7,4), (14,13,3)]
    ]

    kruskal = Kruskal()
    kruskal.execute(graph)
